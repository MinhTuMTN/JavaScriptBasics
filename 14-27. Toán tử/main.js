// Toán tử số học

var a = 5;
var b = 6;

console.log(a + b); // 11
console.log(a - b); // -1
console.log(a * b); // 30
console.log(a / b); // 0.8(3)
console.log(a % b); // 5
console.log(a ** b); // 15625

a++;
console.log(a); // 6

b--;
console.log(b); // 5

// Lưu ý
var c = 8;

console.log(c++);
console.log(c);

